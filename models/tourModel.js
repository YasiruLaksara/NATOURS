const mongoose = require("mongoose");
const slugify = require("slugify"); //before that npm i slugify //not essential//This is for middlwares
const validator = require("validator");

const tourSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: [true, "Tour name is required"],
      unique: true,
      maxlength: [50, "Tour name is required maximum length of 50 characters"],
      minlenght: [5, "Tour name is required minimum length of 5 characters"],
    },

    slug: String,

    duration: {
      type: Number,
      required: [true, "Duration is required"],
    },

    maxGroupSize: {
      type: Number,
      required: [true, "Max group size is required"],
    },

    difficulty: {
      type: String,
      required: [true, "Difficulty is required"],
      enum: {
        values: ["easy", "medium", "hard"],
        message: "Difficulty must be easy, medium, or hard",
      },
    },
    ratingAverage: {
      type: Number,
      default: 4.5,
      min: [1, "Rating must be above 1.0"],
      max: [5, "Rating must be below 5.0"],
    },

    ratingsQuantity: {
      type: Number,
      default: 0,
    },
    price: {
      type: Number,
      required: [true, "Price is required"],
    },

    priceDiscount: {
      type: Number,
      validate: function (val) {
        //custom Validation
        return val < this.price; //this only points to the current document creation
      },
      message: "Discount Price must below the regular Price",
    },

    summary: {
      type: String,
      required: [true, "Summery is required"],
      trim: true,
    },

    description: {
      type: String,
      trim: true,
    },

    imageCover: {
      type: String,
      required: [true, "A tour must contain a cover image"],
    },

    images: {
      type: [String],
    },

    createdAt: {
      type: Date,
      default: Date.now(),
      select: false, // This field will not be outputed as a querry result
    },

    startDates: {
      type: [Date], //Array of dates
    },

    secreatTour: {
      type: Boolean,
      default: false,
    },
  },

  //begining of virtual properties(Business Logics)
  {
    toJSON: { virtuals: true },
    toObject: { virtuals: true },
  }
);

tourSchema.virtual("durationWeeks").get(function () {
  //these duration Weeks are generated by us.not given.for that we use virtual properties
  return this.duration / 7;
});

